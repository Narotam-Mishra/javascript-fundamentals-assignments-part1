
// Unary Operators :-

let x = 5;

x++; // Unary operator: Increment, Result: 6
x--; // Unary operator: Decrement, Result: 5
let y = +"10"; // Unary operator: Unary plus, Result: 10 (converted to a number)
let z = -x; // Unary operator: Unary minus, Result: -5


// Binary Operators :-

let a = 10;
let b = 5;

let sum = a + b; // Binary operator: Addition, Result: 15
let difference = a - b; // Binary operator: Subtraction, Result: 5
let product = a * b; // Binary operator: Multiplication, Result: 50
let quotient = a / b; // Binary operator: Division, Result: 2
let isEqual = a == b; // Binary operator: Equality, Result: false


// Ternary Operator :-

let age = 20;
let canVote = age >= 18 ? "Yes" : "No";
// If age is greater than or equal to 18, canVote will be "Yes"; otherwise, it will be "No"

