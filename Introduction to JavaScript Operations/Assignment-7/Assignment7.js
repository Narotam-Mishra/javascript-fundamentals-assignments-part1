// Write a program in JavaScript to calculate the area of a circle given its radius value of 10. Use appropriate
// arithmetic operators.

// Formula : Area of circle = PI * radius * radius,
//  where PI value is 3.14159

const PI = Math.PI;
let radius = 10;

const areaOfCircle  = PI * radius * radius;
// console.log("value of PI is:", PI);
console.log("Area of Circle:", areaOfCircle);